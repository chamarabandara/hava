        public ApplicationOAuthProvider(string publicClientId, Func<UserManager<AspNetUser>> userManagerFactory)
        {
            if (publicClientId == null)
            {
                throw new ArgumentNullException("publicClientId");
            }

            if (userManagerFactory == null)
            {
                throw new ArgumentNullException("userManagerFactory");
            }

            _publicClientId = publicClientId;
            _userManagerFactory = userManagerFactory;
        }

        public override async Task GrantResourceOwnerCredentials(OAuthGrantResourceOwnerCredentialsContext context)
        {
            //var allowedOrigin = context.OwinContext.Get<string>("as:clientAllowedOrigin");

            //if (allowedOrigin == null) allowedOrigin = "*";

            //context.OwinContext.Response.Headers.Add("Access-Control-Allow-Origin", new[] { allowedOrigin });

            using (AuthRepository _repo = new AuthRepository())
            {

                bool isLoginSuccess =  _repo.CheckGetAspNetUser(context.UserName, context.Password);

                if (!isLoginSuccess)
                {
                    context.SetError("invalid_grant", "The user name or password is incorrect.");
                    return;
                }

                var identity = new ClaimsIdentity(context.Options.AuthenticationType);
                identity.AddClaim(new Claim(ClaimTypes.Name, context.UserName));

                List<string> userPermissions = await _repo.GetUserPermissionsAsync(context.UserName);
                foreach (string permission in userPermissions)
                {
                    identity.AddClaim(new Claim(ClaimTypes.Role, permission));
                }

                double expireTime = 0;
                double.TryParse(ConfigurationManager.AppSettings["TokenTimeOut"].ToString(), out expireTime);


                var props = new AuthenticationProperties(new Dictionary<string, string>
                                                        {
                                                            {
                                                                "as:client_id", (context.ClientId == null) ? string.Empty : context.ClientId
                                                            },
                                                            {
                                                                "userName", context.UserName
                                                            },
                                                            {
                                                                "refreshToken_timeout" , expireTime <= 10 ? "1" : (expireTime - 10).ToString()
                                                            }


                                                        });

                var ticket = new AuthenticationTicket(identity, props);
                context.Validated(ticket);
            }
        }
